    "from email.message import EmailMessage\n",
    "import smtplib\n",
    "import os\n",
    "from datetime import datetime\n",
    "\n",
    "\n",
    "# EMAIL CONFIGURATION\n",
    "\n",
    "# We read the Gmail App Password from system environment\n",
    "# This is safer than writing the password directly in code\n",
    "\n",
    "APP_PASSWORD = os.getenv(\"GMAIL_APP_PASSWORD\")\n",
    "\n",
    "# If the password is missing, stop the script\n",
    "if not APP_PASSWORD:\n",
    "    raise EnvironmentError(\"GMAIL_APP_PASSWORD environment variable not set\")\n",
    "\n",
    "# Get current date and time for email subject and message\n",
    "now = datetime.now().strftime(\"%Y-%m-%d %H:%M\")\n",
    "\n",
    "# Email subject\n",
    "subject = f\"KPI Report | {now}\"\n",
    "\n",
    "# Email body content\n",
    "email_body = f\"\"\"\n",
    "Hi, Pranab\n",
    "\n",
    "Please find attached the KPI report.\n",
    "\n",
    "Generated at: {now}\n",
    "\n",
    "Regards,\n",
    "Automated Analytics Pipeline\n",
    "\"\"\"\n",
    "\n",
    "\n",
    "# READ THE CSV FILE\n",
    "\n",
    "# This CSV was generated by the ETL pipeline\n",
    "# We read it so we can attach it to the email\n",
    "\n",
    "with open(\"restaurant_analytics_final.csv\", \"r\", encoding=\"utf-8\") as f:\n",
    "    csv_data = f.read()\n",
    "\n",
    "# EMAIL MESSAGE SETUP\n",
    "# Create an email message object\n",
    "\n",
    "msg = EmailMessage()\n",
    "msg[\"Subject\"] = subject\n",
    "msg[\"From\"] = \"luxevistahub@gmail.com\"\n",
    "msg[\"To\"] = \"career.pranab@gmail.com\"\n",
    "\n",
    "# Add email body text\n",
    "msg.set_content(email_body)\n",
    "\n",
    "# Attach the CSV file to the email\n",
    "msg.add_attachment(\n",
    "    csv_data.encode(\"utf-8\"),\n",
    "    maintype=\"text\",\n",
    "    subtype=\"csv\",\n",
    "    filename=\"daily_kpis.csv\"\n",
    ")\n",
    "\n",
    "# SMTP CONNECTION\n",
    "\n",
    "# SMTP is the system used to send emails over the internet\n",
    "# Gmail provides its own SMTP server\n",
    "\n",
    "server = smtplib.SMTP_SSL(\"smtp.gmail.com\", 465)\n",
    "\n",
    "# Login using Gmail email ID and App Password\n",
    "server.login(\"luxevistahub@gmail.com\", APP_PASSWORD)\n",
    "\n",
    "\"\"\"\n",
    "#Login with the sender id\n",
    "server.login(\n",
